EOFCONFIG
    echo "âœ… Default configuration created"
fi

echo "ğŸ“‹ Current clipboard settings:"
echo "=============================="
grep -E "^(MaxClipItems|PreventEmptyClipboard|IgnoreSelection|IgnoreImages)" "$KLIPPER_CONFIG" 2>/dev/null || echo "No settings found"
echo ""

echo "ğŸ”§ Available actions:"
echo "1. Edit configuration file directly"
echo "2. Reset to defaults"
echo "3. Show current settings"
echo "4. Restart clipboard service"
echo ""

read -p "Choose action (1-4): " choice

case $choice in
    1)
        ${EDITOR:-nano} "$KLIPPER_CONFIG"
        echo "âœ… Configuration edited. Restart KDE session to apply changes."
        ;;
    2)
        cp "$KLIPPER_CONFIG" "$KLIPPER_CONFIG.backup"
        # Recreate default config
        cat > "$KLIPPER_CONFIG" << 'EOFCONFIG'
[General]
KeepClipboardContents=true
MaxClipItems=20
PreventEmptyClipboard=true
IgnoreSelection=false
IgnoreImages=false
SynchronizeClipboardAndSelection=true
ActionsEnabled=true
StripWhiteSpace=true
SaveClipboardContents=true
EOFCONFIG
        echo "âœ… Configuration reset to defaults"
        ;;
    3)
        echo "ğŸ“‹ Full configuration:"
        cat "$KLIPPER_CONFIG"
        ;;
    4)
        echo "ğŸ”„ Restarting clipboard service..."
        qdbus org.kde.klipper /klipper org.kde.klipper.klipper.quit 2>/dev/null || true
        sleep 2
        klipper &
        echo "âœ… Clipboard service restarted"
        ;;
    *)
        echo "âŒ Invalid choice"
        ;;
esac
EOF
    
    chmod +x "$config_script"
    log_message "OK" "âœ… Alternative configuration script created: $config_script"
    
    # Create desktop file for easy access
    local desktop_file="$HOME/.local/share/applications/configure-clipboard.desktop"
    mkdir -p "$(dirname "$desktop_file")"
    
    cat > "$desktop_file" << EOF
[Desktop Entry]
Type=Application
Name=Configure Clipboard
Comment=Alternative clipboard configuration (fixes broken Configure button)
Exec=$config_script
Icon=edit-copy
Categories=Settings;System;
StartupNotify=true
Keywords=clipboard;klipper;configuration;settings;
EOF
    
    log_message "OK" "âœ… Desktop file created: $desktop_file"
}

# â”€â”€â”€ RESTART CLIPBOARD SERVICES â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
# WHAT: Restart clipboard-related services to apply configuration changes
# WHY: Configuration changes require service restart to take effect
# HOW: Safely restart klipper and plasma shell clipboard components
restart_clipboard_services() {
    log_message "STEP" "ğŸ”„ Restarting clipboard services..."
    
    # Stop existing klipper
    qdbus org.kde.klipper /klipper org.kde.klipper.klipper.quit 2>/dev/null || true
    pkill -f klipper 2>/dev/null || true
    sleep 2
    
    # Start klipper
    if command -v klipper >/dev/null; then
        klipper &
        sleep 2
        
        if pgrep -f klipper >/dev/null; then
            log_message "OK" "âœ… Klipper restarted successfully"
        else
            log_message "ERROR" "âŒ Failed to restart Klipper"
        fi
    else
        log_message "WARN" "âš ï¸ Klipper command not found"
    fi
    
    # Restart plasmashell to reload widget configuration
    log_message "INFO" "ğŸ”„ Restarting Plasma shell to apply widget changes..."
    qdbus org.kde.plasmashell /PlasmaShell org.kde.PlasmaShell.restart 2>/dev/null || {
        log_message "WARN" "âš ï¸ Could not restart Plasma shell via D-Bus"
        log_message "INFO" "ğŸ’¡ Manual restart recommended: kquitapp6 plasmashell && plasmashell &"
    }
}

# â”€â”€â”€ MAIN EXECUTION â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
main() {
    log_message "START" "ğŸ¯ Starting clipboard widget configuration fix..."
    
    # Step 1: Diagnose issues
    log_message "PHASE" "ğŸ” Phase 1: Issue Diagnosis"
    if ! diagnose_clipboard_issue; then
        log_message "INFO" "ğŸ“Š Issues found - proceeding with fixes"
    fi
    
    # Step 2: Create proper Klipper configuration
    log_message "PHASE" "ğŸ“ Phase 2: Configuration Creation"
    create_klipper_config
    
    # Step 3: Fix system tray widget
    log_message "PHASE" "ğŸ”§ Phase 3: Widget Configuration"
    fix_systemtray_widget
    
    # Step 4: Create alternative configuration method
    log_message "PHASE" "ğŸ› ï¸ Phase 4: Alternative Configuration"
    create_alternative_config
    
    # Step 5: Restart services
    log_message "PHASE" "ğŸ”„ Phase 5: Service Restart"
    restart_clipboard_services
    
    log_message "COMPLETE" "ğŸ‰ Clipboard widget configuration fix complete!"
    
    echo ""
    echo "ğŸ¯ CLIPBOARD WIDGET CONFIGURATION FIX COMPLETE!"
    echo "=============================================="
    echo ""
    echo "âœ… FIXES APPLIED:"
    echo "  â€¢ Created proper Klipper configuration file"
    echo "  â€¢ Fixed system tray widget visibility"
    echo "  â€¢ Created alternative configuration method"
    echo "  â€¢ Restarted clipboard services"
    echo ""
    echo "ğŸ”§ ALTERNATIVE CONFIGURATION ACCESS:"
    echo "  â€¢ Command: ~/.local/bin/configure-clipboard"
    echo "  â€¢ Desktop: Search for 'Configure Clipboard' in app launcher"
    echo "  â€¢ Direct edit: $KLIPPER_CONFIG"
    echo ""
    echo "ğŸ’¡ IF 'CONFIGURE CLIPBOARD' STILL FAILS:"
